public class Ex3{
    public static void main (String[] args){
        Java javaobj = new Java("Java 11"); // objeto(javaobj) -> tem valores, atributos definidos na classe e pode chamar os métodos definidos na classe
        System.out.println(javaobj.getNome()); // metodo(getNome) -> funçao associada a classe que define comportamentos ''açoes'' para os objetos dessa classe 
    }
}
    
    /* CONCEITOS:
        objeto(javaobj) -> tem valores, atributos definidos na classe e pode chamar os métodos definidos na classe
        metodo(getNome) -> funçao associada a classe que define comportamentos ''açoes'' para os objetos dessa classe 
        classe(Java) -> define atributos e metodos que os objetos terao */